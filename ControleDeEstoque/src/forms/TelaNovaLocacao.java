    public Locacao execute() {

        for (int i = 0; i < CarroDados.lstCarros.size(); i++) {
            cmbEscolherCarro.addItem(CarroDados.lstCarros.get(i).getMarca() + " " + CarroDados.lstCarros.get(i).getModelo());
        }
        for (int i = 0; i < ClienteDados.lstClientes.size(); i++) {
            cmbEscolherCliente.addItem(ClienteDados.lstClientes.get(i).getNome());
        }

        this.setVisible(true);
        return this.locacaoTemp;
    }

